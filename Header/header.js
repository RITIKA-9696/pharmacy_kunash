function setupSearch() {
  console.log("[SEARCH] Initializing search overlay");

  const overlay = document.getElementById('search-overlay');
  if (!overlay) {
    console.error("[SEARCH FATAL] #search-overlay element NOT FOUND");
    return;
  }

  const closeBtn = document.getElementById('search-close-btn');
  const desktopBtn = document.getElementById('desktop-search-btn');
  const mobileBtn = document.getElementById('mobile-search-btn');

  console.log("[SEARCH] Elements:", {
    overlay: !!overlay,
    closeBtn: !!closeBtn,
    desktopBtn: !!desktopBtn,
    mobileBtn: !!mobileBtn
  });

  function openSearchOverlay(e) {
    if (e) {
      e.preventDefault();
      e.stopPropagation();
    }
    console.log("[OVERLAY] Opening search overlay");
    overlay.classList.remove('hidden');
    overlay.classList.add('flex');

    setTimeout(() => {
      const input = document.getElementById('search-input-field');
      if (input) {
        input.focus();
        input.value = '';
      }
      const suggestions = document.getElementById('search-suggestions');
      if (suggestions) {
        suggestions.style.display = 'none';
        suggestions.innerHTML = '';
      }
      const popular = document.getElementById('popular-categories');
      if (popular) popular.style.display = 'block';
    }, 100);
  }

  function closeSearchOverlay() {
    console.log("[OVERLAY] Closing search overlay");
    overlay.classList.add('hidden');
    overlay.classList.remove('flex');
  }

  if (desktopBtn) desktopBtn.addEventListener('click', openSearchOverlay);
  if (mobileBtn) mobileBtn.addEventListener('click', openSearchOverlay);

  document.querySelectorAll('.fa-search').forEach(icon => {
    icon.addEventListener('click', e => {
      console.log("[OVERLAY] Search icon clicked");
      e.preventDefault();
      e.stopPropagation();
      openSearchOverlay();
    });
  });

  if (closeBtn) closeBtn.addEventListener('click', closeSearchOverlay);

  document.addEventListener('keydown', e => {
    if (e.key === 'Escape' && overlay.classList.contains('flex')) {
      closeSearchOverlay();
    }
  });

  overlay.addEventListener('click', e => {
    if (e.target === overlay) closeSearchOverlay();
  });

  // ────────────────────────────────────────────────
  //   PRODUCT SEARCH LOGIC
  // ────────────────────────────────────────────────

  let globalSearchProducts = [];
  const API_BASE = "http://localhost:8083";
  const FALLBACK_IMAGE = "http://localhost:8083/Images/product_details_fallback_img.jpg";

  async function loadGlobalSearchData(forceRefresh = false) {
    if (!forceRefresh && globalSearchProducts.length > 0) {
      console.log("[SEARCH-DATA] Using cached products");
      return;
    }
    console.log("[SEARCH-DATA] Loading products...");
    globalSearchProducts = [];

    try {
      await fetchAllPages('/api/products/get-all-active-products', mapRegularProduct);
      await fetchAllPages('/api/mb/products/get-all-mb-active-products', mapMBProduct);
      console.log("[SEARCH-DATA] Loaded:", globalSearchProducts.length, "products");
    } catch (err) {
      console.error("[SEARCH-DATA ERROR]", err);
    }
  }

  async function fetchAllPages(endpointPath, mapper) {
    let page = 0;
    let hasMore = true;
    while (hasMore) {
      try {
        const url = `${API_BASE}${endpointPath}?page=${page}&size=50`;
        const res = await fetch(url);
        if (!res.ok) break;
        const data = await res.json();
        const items = data.content || data || [];
        if (items.length === 0) break;
        items.forEach(item => globalSearchProducts.push(mapper(item)));
        hasMore = items.length === 50;
        page++;
      } catch (err) {
        console.warn("[FETCH WARN]", endpointPath, page, err);
        break;
      }
    }
  }

  function mapRegularProduct(p) {
    return {
      id: p.productId,
      name: p.productName || 'Unknown',
      subCategory: (p.productSubCategory || 'Product').trim(),
      price: Array.isArray(p.productPrice) ? Number(p.productPrice[0] || 0) : Number(p.productPrice || 0),
      image: p.productMainImage || FALLBACK_IMAGE,
      detailUrl: `/productdetails.html?id=${p.productId}`,
      type: 'regular'
    };
  }

  function mapMBProduct(p) {
    const catNorm = (p.category || '').trim().toLowerCase();
    let detailPath = '/MotherCare/mother-product-details.html';
    if (catNorm.includes('baby')) detailPath = '/BabyCare/baby-product-details.html';
    return {
      id: p.id,
      name: p.title || 'Unknown MB',
      subCategory: (p.subCategory || 'MB Product').trim(),
      price: Array.isArray(p.price) && p.price.length > 0 ? Number(p.price[0]) : 0,
      image: p.mainImageUrl || FALLBACK_IMAGE,
      detailUrl: `${detailPath}?id=${p.id}`,
      type: 'mbp',
      category: p.category
    };
  }

  function highlightText(text, query) {
    if (!query) return text;
    const esc = query.trim().replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    const regex = new RegExp(`(${esc})`, 'gi');
    return text.replace(regex, '<span class="highlight">$1</span>');
  }

  function initSearchFunctionality() {
    const input = document.getElementById('search-input-field');
    const suggestions = document.getElementById('search-suggestions');
    const popular = document.getElementById('popular-categories');

    if (!input || !suggestions) {
      console.error("[SEARCH] Missing input or suggestions container");
      return;
    }

    let isMouseOver = false;
    let debounceTimer;

    suggestions.addEventListener('mouseenter', () => isMouseOver = true);
    suggestions.addEventListener('mouseleave', () => isMouseOver = false);

    document.addEventListener('click', e => {
      if (!suggestions.contains(e.target) && e.target !== input && !isMouseOver) {
        suggestions.style.display = 'none';
      }
    });

    input.addEventListener('input', e => {
      const val = e.target.value.trim();
      console.log(`[SEARCH INPUT] "${val}"`);

      clearTimeout(debounceTimer);

      if (val.length < 2) {
        suggestions.style.display = 'none';
        if (popular) popular.style.display = 'block';
        return;
      }

      if (popular) popular.style.display = 'none';

      debounceTimer = setTimeout(() => {
        showSearchSuggestions(val);
      }, 300);
    });

    input.addEventListener('keypress', e => {
      if (e.key === 'Enter') {
        const q = input.value.trim();
        if (q.length >= 2) showSearchSuggestions(q);
      }
    });

    input.addEventListener('focus', () => {
      if (input.value.trim().length < 2 && popular) {
        suggestions.style.display = 'none';
        popular.style.display = 'block';
      }
    });

    console.log("[SEARCH] Input listeners attached");
  }

  async function showSearchSuggestions(query) {
    console.log(`[SEARCH SUGGEST] Query: "${query}"`);

    const suggestions = document.getElementById('search-suggestions');
    suggestions.style.display = 'block';
    suggestions.innerHTML = '<div class="p-4 text-center text-gray-500">Searching...</div>';

    await loadGlobalSearchData(true);

    const q = query.toLowerCase().trim();
    const results = [];
    const seen = new Set();

    for (const p of globalSearchProducts) {
      if (seen.has(p.id)) continue;
      seen.add(p.id);

      const n = (p.name || '').toLowerCase();
      const s = (p.subCategory || '').toLowerCase();
      const c = (p.category || '').toLowerCase();

      let score = 0;
      if (n.includes(q)) score += 10;
      if (s.includes(q)) score += 6;
      if (c.includes(q)) score += 5;
      if (new RegExp(`\\b${q.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`, 'i').test(n)) score += 8;

      if (q.includes('baby')   && (s.includes('baby')   || c.includes('baby')))   score += 4;
      if (q.includes('mother') && (s.includes('mother') || s.includes('matern') || c.includes('mother'))) score += 4;
      if (q.includes('skin')   && s.includes('skin')) score += 3;
      if (q.includes('vitamin')&& (s.includes('vitamin')|| n.includes('vitamin'))) score += 5;
      if (q.includes('dettol') && n.includes('dettol')) score += 10;

      if (score >= 5) results.push({product: p, score});
    }

    console.log(`[SEARCH] Found ${results.length} candidates`);

    results.sort((a, b) => b.score - a.score);
    const top = results.slice(0, 10).map(r => r.product);

    if (top.length === 0) {
      suggestions.innerHTML = '<div class="p-4 text-center text-gray-500">No matches found.<br>Try: baby oil, vitamin c, paracetamol</div>';
      return;
    }

    suggestions.innerHTML = top.map(p => `
      <div class="suggestion-item group flex items-center p-4 border-b hover:bg-gray-50 cursor-pointer"
           data-url="${p.detailUrl}">
        <img src="${p.image.startsWith('http') ? p.image : API_BASE + p.image}"
             class="w-12 h-12 object-cover rounded mr-3"
             alt="${p.name}"
             onerror="this.src='${FALLBACK_IMAGE}'">
        <div class="flex-1">
          <div class="font-medium group-hover:text-pink-600">${highlightText(p.name, query)}</div>
          <div class="text-sm text-gray-500">${p.subCategory || p.category || '?'}</div>
          <div class="text-sm font-medium text-pink-600">₹${p.price || '?'}</div>
        </div>
        <i class="fas fa-arrow-right text-pink-600 opacity-0 group-hover:opacity-100"></i>
      </div>
    `).join('');

    suggestions.querySelectorAll('.suggestion-item').forEach(el => {
      el.addEventListener('click', e => {
        e.preventDefault();
        e.stopPropagation();
        const url = el.dataset.url;
        console.log("[SEARCH] → Redirecting to", url);
        window.location.href = url;
      });
    });
  }

  // Start search system
  initSearchFunctionality();
  setTimeout(() => loadGlobalSearchData(), 1200);
}

