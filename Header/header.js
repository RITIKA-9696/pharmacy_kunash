function loadSavedPincode() {
  const savedPin = localStorage.getItem('savedPincode');
  const savedLocation = localStorage.getItem('savedLocation');

  if (savedPin && savedLocation) {
    document.getElementById('display-location').textContent = savedLocation;
    document.getElementById('display-pincode').textContent = savedPin;
    document.getElementById('display-pincode').classList.remove('hidden');
    document.getElementById('delivery-info').classList.remove('hidden');
  } else {
    localStorage.removeItem('savedPincode');
    localStorage.removeItem('savedLocation');
    document.getElementById('display-location').textContent = 'Select Location';
    document.getElementById('display-pincode').classList.add('hidden');
    document.getElementById('delivery-info').classList.add('hidden');
  }
}




async function validateAndSavePincode() {
  const input = document.getElementById('modal-pincode-input');
  const pin = input.value.trim();
  const error = document.getElementById('pincode-error');
  const success = document.getElementById('pincode-success');
  const checkBtn = document.getElementById('check-pincode-btn');

  error.classList.add('hidden');
  success.classList.add('hidden');
  input.classList.remove('pincode-invalid');

  if (!/^\d{6}$/.test(pin)) {
    error.textContent = "Please enter a valid 6-digit pincode";
    error.classList.remove('hidden');
    input.classList.add('pincode-invalid');
    input.focus();
    return;
  }

  checkBtn.disabled = true;
  checkBtn.innerHTML = '<i class="fas fa-spinner fa-spin mr-2"></i> Checking...';

  // Skip actual verification for development
  setTimeout(() => {
    const locationName = `Pincode: ${pin}`;
    localStorage.setItem('savedPincode', pin);
    localStorage.setItem('savedLocation', locationName);

    document.getElementById('display-location').textContent = locationName;
    document.getElementById('display-pincode').textContent = pin;
    document.getElementById('display-pincode').classList.remove('hidden');
    document.getElementById('delivery-info').classList.remove('hidden');

    success.innerHTML = `
      <i class="fas fa-check-circle mr-2"></i>
      Delivery available for pincode: <strong>${pin}</strong>
    `;
    success.classList.remove('hidden');

    setTimeout(() => {
      closePincodeModal();
      showNotification(`Delivering to ${locationName}`);
    }, 1500);

    checkBtn.disabled = false;
    checkBtn.textContent = 'Check';
  }, 1000);
}

