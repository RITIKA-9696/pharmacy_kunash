function setupMobileMenu() {
  const mobileMenuBtn = document.getElementById('mobile-menu-btn');
  const mobileMenu = document.getElementById('mobile-menu');

  if (!mobileMenuBtn || !mobileMenu) return;

  mobileMenuBtn.addEventListener('click', (e) => {
    e.stopPropagation();
    e.preventDefault();

    if (mobileMenu.classList.contains('hidden')) {
      mobileMenu.classList.remove('hidden');
      document.body.style.overflow = 'hidden';
    } else {
      mobileMenu.classList.add('hidden');
      document.body.style.overflow = '';
    }
  });

  document.addEventListener('click', (e) => {
    if (!mobileMenu.contains(e.target) && 
        !mobileMenuBtn.contains(e.target) && 
        !mobileMenu.classList.contains('hidden')) {
      mobileMenu.classList.add('hidden');
      document.body.style.overflow = '';
    }
  });

  document.querySelectorAll('.mobile-dropdown > button').forEach(btn => {
    btn.addEventListener('click', function(e) {
      e.stopPropagation();
      const content = this.nextElementSibling;
      content.classList.toggle('hidden');
    });
  });
}

// =============== CART COUNT FROM BACKEND API ===============

async function updateCartCountFromBackend() {
  const userData = localStorage.getItem('currentUser') || sessionStorage.getItem('currentUser');
  
  if (!userData) {
    updateCartBadge(0);
    return;
  }

  let user;
  try {
    user = JSON.parse(userData);
  } catch (e) {
    updateCartBadge(0);
    return;
  }

  const userId = user.userId || user.id || user.userID || user._id;

  if (!userId) {
    updateCartBadge(0);
    return;
  }

  try {
    const apiUrl = `http://localhost:8083/api/cart/cart-count-by-userId/${userId}`;

    const response = await fetch(apiUrl, {
      method: 'GET',
      credentials: 'include',
      cache: 'no-cache'
    });

    if (response.ok) {
      const countText = await response.text();
      const count = parseInt(countText.trim()) || 0;
      updateCartBadge(count);
    } else {
      fallbackLocalCartCount();
    }
  } catch (err) {
    fallbackLocalCartCount();
  }
}

function updateCartBadge(count) {
  const selectors = '#desktop-cart-count, #mobile-cart-count, #cartCount, .cart-count';
  document.querySelectorAll(selectors).forEach(el => {
    if (el) {
      el.textContent = count;
      if (count > 0) {
        el.style.display = 'flex';
        el.classList.remove('hidden');
      } else {
        el.style.display = 'none';
        el.classList.add('hidden');
      }
    }
  });
}

function fallbackLocalCartCount() {
  const cart = JSON.parse(localStorage.getItem('cart') || '[]');
  const count = cart.length;
  updateCartBadge(count);
}

